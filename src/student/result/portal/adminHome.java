    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String Course = (String)course.getSelectedItem();
        String Branch = (String)branch.getSelectedItem();
        String RollNumber = (String)rollNumber.getText();
        String Name = (String)name.getText();
        String Gender = (String)gender.getSelectedItem();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/navin", "root", "Naveen$98");
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollnumber, name, course, branch, gender) values('"+RollNumber+"', '"+Name+"', '"+Course+"', '"+Branch+"', '"+Gender+"')");
            JOptionPane.showMessageDialog(null, "Data Saved Sucessfully");
            setVisible(false);
            new adminHome().setVisible(true);
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
        
        
    }//GEN-LAST:event_saveActionPerformed

